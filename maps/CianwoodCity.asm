CianwoodCitySailor:
	faceplayer
	opentext
	checkevent EVENT_UNION_CAVE_SLOWPOKE_WELL_LINK_PICKAXE
	iftrue .CheckBalls
	checkevent EVENT_GOT_OLD_ROD
	iftrue .CheckBalls
	checkevent EVENT_GOT_GOOD_ROD
	iftrue .CheckBalls
	checkevent EVENT_GOT_SUPER_ROD
	iftrue .CheckBalls
	checkevent EVENT_GOT_SOFT_SAND_FROM_KATE
	iftrue .CheckBalls
	setval TENTACOOL
	special MonCheck
	iftrue .Alola
	writetext CianwoodCitySailorTentacoolText
	yesorno
	iftrue .GiveTentacool
	sjump .Done

.GiveTentacool:
	writetext PlayerReceivedTentacoolText
	playsound SFX_CAUGHT_MON
	waitsfx
	promptbutton
	givepoke TENTACOOL, 10
	sjump .Done

.GiveBalls:
	writetext CianwoodCitySailorPreparedText
	promptbutton
	verbosegiveitemfive POKE_BALL, 5
	sjump .Done2

.CheckBalls:
	checkitem MASTER_BALL
	iftrue .Alola
	checkitem ULTRA_BALL
	iftrue .Alola
	checkitem GREAT_BALL
	iftrue .Alola
	checkitem TIMER_BALL
	iftrue .Alola
	checkitem DUSK_BALL
	iftrue .Alola
	checkitem HEAVY_BALL
	iftrue .Alola
	checkitem LEVEL_BALL
	iftrue .Alola
	checkitem LURE_BALL
	iftrue .Alola
	checkitem FAST_BALL
	iftrue .Alola
	checkitem FRIEND_BALL
	iftrue .Alola
	checkitem MOON_BALL
	iftrue .Alola
	checkitem LOVE_BALL
	iftrue .Alola
	checkitem POKE_BALL
	iffalse .GiveBalls
	; fallthrough

.Alola
	writetext CianwoodCitySailorAlolaText
.Done
	waitbutton
.Done2
	closetext
	end

